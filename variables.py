# Variable : programs use variables to remember information
#           if we want a variable name with multiple words, use snake case

# = : we use = sign to store a value inside it

# Lines of code are instructions for the computer to follow

# print() : we tell the computer to display a value in an area called te console

name = "Chloe"
print(name)
print("Hello, World!")


# Updating variables : the values they store can change
status = "Relaxing"
status = "Watching Netflix"
print(status)

status = "Reading a book"
print(status)


# We can also give variables the values of other variables

default_option = "upload"
new_status = "download"

new_status = default_option
print(new_status)